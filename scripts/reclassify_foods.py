# '메인+밥' 형태(떡갈비콩나물밥 등)도 한그릇 (사용자 결정)
MAIN_PLUS_RICE = ("떡갈비", "너비아니", "불고기말이", "쇠고기말이")


def reclass_rice(name: str) -> str | None:
    """'밥' 역할 음식이 실제로는 한그릇이면 '한그릇' 반환. 아니면 None(밥 유지)."""
    if any(k in name for k in COMPLETE_RICE_KEYWORDS):
        return "한그릇"
    if any(k in name for k in MAIN_PLUS_RICE):
        return "한그릇"
    return None


def main() -> None:
    dry = "--dry-run" in sys.argv
    df = pd.read_csv(CSV)
    before = len(df)

    delete_set = (
        set(A_DELETE) | set(B_DELETE) | set(C_DELETE) | set(EXTRA_DELETE) | set(JUNK_DELETE)
    )

    # 삭제
    to_drop = df["food_name"].isin(delete_set)
    dropped_names = sorted(df[to_drop]["food_name"].unique().tolist())
    df = df[~to_drop].copy()

    # A 재분류
    onedish_set = set(A_TO_ONEDISH)
    banchan_set = set(A_TO_BANCHAN)
    df.loc[df["food_name"].isin(onedish_set), "meal_role"] = "한그릇"
    df.loc[df["food_name"].isin(banchan_set), "meal_role"] = "반찬"

    # 밥 → 한그릇 재분류
    rice_moved = []
    rice_mask = df["meal_role"] == "밥"
    for idx in df[rice_mask].index:
        name = df.at[idx, "food_name"]
        new = reclass_rice(name)
        if new:
            df.at[idx, "meal_role"] = new
            rice_moved.append(name)

    after = len(df)
    remaining_rice = sorted(df[df["meal_role"] == "밥"]["food_name"].tolist())

    # 리포트
    lines = [
        "# 데이터 재정제 리포트",
        "",
        f"- 원본: {before}개 → 정제 후: {after}개 (삭제 {before - after}개)",
        "",
        "## 삭제된 음식 (양식·퓨전·이상식감)",
        f"총 {len(dropped_names)}개",
        *[f"- {n}" for n in dropped_names],
        "",
        "## '밥' → '한그릇' 재분류 (완결형 한 그릇)",
        f"총 {len(rice_moved)}개",
        *[f"- {n}" for n in sorted(rice_moved)],
        "",
        "## '밥'으로 최종 유지 (진짜 밥류)",
        f"총 {len(remaining_rice)}개",
        *[f"- {n}" for n in remaining_rice],
        "",
        "## meal_role 최종 분포",
        "```",
        df["meal_role"].value_counts().to_string(),
        "```",
    ]
    report = "\n".join(lines)

    if dry:
        print(report)
        print("\n[dry-run] 파일은 저장하지 않았습니다.")
        return

    # 백업 후 저장
    pd.read_csv(CSV).to_csv(BACKUP, index=False)
    df.to_csv(CSV, index=False)
    REPORT.write_text(report, encoding="utf-8")
    print(f"완료: {before} → {after}개")
    print(f"  삭제 {len(dropped_names)} / 밥→한그릇 {len(rice_moved)}")
    print(f"  백업: {BACKUP.name} / 리포트: {REPORT}")
    print("\n최종 meal_role 분포:")
    print(df["meal_role"].value_counts().to_string())


if __name__ == "__main__":
    main()
